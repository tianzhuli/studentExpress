package com.me.domain;

public class DiscountCoupon {
	private int discountCouponID;
	private String walletID;
	private String discountCouponName;
	private String discountCouponFunction;
	public int getDiscountCouponID() {
		return discountCouponID;
	}
	public void setDiscountCouponID(int discountCouponID) {
		this.discountCouponID = discountCouponID;
	}
	public String getWalletID() {
		return walletID;
	}
	public void setWalletID(String walletID) {
		this.walletID = walletID;
	}
	public String getDiscountCouponName() {
		return discountCouponName;
	}
	public void setDiscountCouponName(String discountCouponName) {
		this.discountCouponName = discountCouponName;
	}
	public String getDiscountCouponFunction() {
		return discountCouponFunction;
	}
	public void setDiscountCouponFunction(String discountCouponFunction) {
		this.discountCouponFunction = discountCouponFunction;
	}
	
}
